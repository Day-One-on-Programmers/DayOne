//빈칸에 알맞은 타입을 채워 문제를 해결해 보세요.
//
//기본 타입에는 값 종류에 따라, 정수, 문자, 실수, 논리 타입이 있습니다.
//
//        정수 : byte, short, int, long
//실수 : float, double
//문자 : char
//논리 : boolean
//값의 크기에 따라 타입을 달리 선언해야 하는데요.
//이 부분을 주의해서 빈칸에 타입을 채워보세요.


public class M3 {
    public static void main(String[] args) {

        int intValue = 123456;
        long longValue = 123456789999L;
        float floatValue = 1.234F;
        char charValue = '가';
        boolean booleanFun = false;

        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(charValue);
        System.out.println(booleanFun);
    }
}
